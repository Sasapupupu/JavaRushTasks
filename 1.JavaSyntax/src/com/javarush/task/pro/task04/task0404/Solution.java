package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        int j = 5;
        while (j > 0) {
            int i = 10;
            while (i > 0) {
                System.out.print("Q");
                i--;
            }
            System.out.println("");
            j--;
        }
    }
}